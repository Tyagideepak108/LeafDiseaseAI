# --- Function: TTS ---
def text_to_speech(text, lang):
    if not text:
        return None
    try:
        tts = gTTS(text, lang=lang)
        audio_bytes_io = io.BytesIO()
        tts.write_to_fp(audio_bytes_io)
        return audio_bytes_io
    except Exception as e:
        st.error(f"тЭМ Error generating audio: {e}")
        return None

